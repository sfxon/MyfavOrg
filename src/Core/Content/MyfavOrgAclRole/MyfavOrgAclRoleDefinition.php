class MyfavOrgAclRoleDefinition extends EntityDefinition
{
    public const ENTITY_NAME = 'myfav_org_acl_role';

    /**
     * getEntityName
     *
     * @return string
     */
    public function getEntityName(): string
    {
        return self::ENTITY_NAME;
    }

    /**
     * getEntityClass
     *
     * @return string
     */
    public function getEntityClass(): string
    {
        return PimImageEntity::class;
    }

    /**
     * getCollectionClass
     *
     * @return string
     */
    public function getCollectionClass(): string
    {
        return PimImageCollection::class;
    }

    /**
     * defineFields
     *
     * @return FieldCollection
     */
    protected function defineFields(): FieldCollection
    {
        return new FieldCollection([
            (new IdField('id', 'id'))->addFlags(new Required(), new PrimaryKey(), new ApiAware()),
            (new StringField('name', 'name'))->addFlags(new Required()),

            new OneToManyAssociationField('myfav_org_acl_role_attributes', MyfavOrgAclRoleAttributeDefinition::class, 'myfav_org_acl_rule_id')
        ]);
    }
}
